package object;

public class VolumeSMF {
	
	private String date;
	private int volumeGB; //volume di SMF elaborato in gigabyte
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getVolumeGB() {
		return volumeGB;
	}
	public void setVolumeGB(int volumeGB) {
		this.volumeGB = volumeGB;
	}

}
