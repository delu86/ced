package object;

public class CECReport {

	private String serial;
	private String date;
	private int hour;
	private int msu4hra;//media mobile 4 ore msu
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getMsu4hra() {
		return msu4hra;
	}
	public void setMsu4hra(int msu4hra) {
		this.msu4hra = msu4hra;
	}
}
